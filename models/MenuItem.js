let mongoose = require("mongoose");
let Schema = mongoose.Schema;

const MenuItem = new Schema({
  name: {
    type: String,
    required: true
  },
  category: {
    type: String,
    required: true
  },
  description: {
    type: String,
    required: true
  },
  price: {
    type: Number,
    required: true
  }
});

module.exports = mongoose.model("MenuItem", MenuItem);
